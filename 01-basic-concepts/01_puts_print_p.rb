# typed: strict
# puts method
# puts method is used to print the output on the screen
# and it automatically adds a new line at the end of the output

puts 'Hello World'

# print method
# print method is used to print the output on the screen
# and it does not add a new line at the end of the output
print 'Hello World'
print 'Hello World'
print 'Hello World'

# only puts adds a new line
puts

# p method
# p method is used to print the output on the screen
# and it provides a detailed information about the object
# it not add a new line at the end of the output
p 'Hello World'
