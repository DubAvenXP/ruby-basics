today = Time.now # current date and time

p today # yyyy-mm-dd hh:mm:ss +zzzz
p today.month # mm
p today.day # dd
p today.year # yyyy
p today.wday # day of the week (0-6)
p today.yday # day of the year (1-366)
p today.hour # hh
p today.min # mm
p today.sec # ss
p today.usec # microseconds
p today.zone # timezone
