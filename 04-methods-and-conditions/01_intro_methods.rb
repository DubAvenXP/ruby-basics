def introduce_myself(name, age)
    greeting = 'Hello'

    puts "#{greeting}, my name is #{name} and I am #{age} years old."

    puts greeting
    puts "Hello, my name is #{name}"
    puts 'I am handsome'
    puts 'I am talented'
    puts 'I am brilliant'
end

introduce_myself('Alejandro', 23)
introduce_myself('Martin', 56)
